void UGCFSMStateNode::OnRenameNode(const FString& newName)
{
	NodeName = (newName == GetDefaultTitle().ToString() || newName == SuggestedNodeName()) ? FString {} : newName;

	if (auto schema = GetSchema())
	{
		schema->ForceVisualizationCacheClear();
	}
}

bool UGCFSMStateNode::HasImplementationClass() const
{
	// the extra check about Pins.Num() is necessary, because this function might be called before allocating the pins
	return Pins.Num() && (Pins[ImplementationPinIndex]->DefaultObject || Pins[ImplementationPinIndex]->LinkedTo.Num() > 0);
}

UClass* UGCFSMStateNode::GetImplementationClass() const
{
	// the extra check about Pins.Num() is necessary, because this function might be called before allocating the pins
	return Pins.Num() && Pins[ImplementationPinIndex]->LinkedTo.Num() == 0 ? Cast<UClass>(Pins[ImplementationPinIndex]->DefaultObject) : nullptr;
}

