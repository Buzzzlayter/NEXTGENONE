void UGCFSMStateNode::CreatePinsForClass(UClass* implementationClass, TArray<UEdGraphPin*>* classPins)
{
	// based on UK2Node_ConstructObjectFromClass::CreatePinsForClass

	if (implementationClass)
	{
		auto schema = GetDefault<UEdGraphSchema_K2>();
		auto classDefaultObject = implementationClass->GetDefaultObject(false);
		for (TFieldIterator<FProperty> propertyIt(implementationClass, EFieldIteratorFlags::IncludeSuper); propertyIt; ++propertyIt)
		{
			auto property = *propertyIt;
			const bool bIsDelegate = property->IsA(FMulticastDelegateProperty::StaticClass());
			const bool bIsExposedToSpawn = UEdGraphSchema_K2::IsPropertyExposedOnSpawn(property);
			const bool bIsSettableExternally = !property->HasAnyPropertyFlags(CPF_DisableEditOnInstance);

			if (bIsExposedToSpawn &&
				!property->HasAnyPropertyFlags(CPF_Parm) &&
				bIsSettableExternally &&
				property->HasAllPropertyFlags(CPF_BlueprintVisible) &&
				!bIsDelegate &&
				(nullptr == FindPin(property->GetName())) &&
				FBlueprintEditorUtils::PropertyStillExists(property))
			{
				if (UEdGraphPin* pin = CreatePin(EGPD_Input, {}, {}, nullptr, property->GetFName()))
				{
					schema->ConvertPropertyToPinType(property, pin->PinType);
					if (classPins)
					{
						classPins->Add(pin);
					}

					if (classDefaultObject && schema->PinDefaultValueIsEditable(*pin))
					{
						FString defaultValueAsString;
						verify(FBlueprintEditorUtils::PropertyValueToString(property, reinterpret_cast<const uint8*>(classDefaultObject), defaultValueAsString));
						schema->SetPinAutogeneratedDefaultValue(pin, defaultValueAsString);
					}

					schema->ConstructBasicPinTooltip(*pin, property->GetToolTipText(), pin->PinToolTip);
				}
			}
		}
	}
}

