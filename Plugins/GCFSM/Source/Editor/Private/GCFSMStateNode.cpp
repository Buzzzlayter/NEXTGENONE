	{
		UGCFSMStateNode* stateNode = CastChecked<UGCFSMStateNode>(GraphNode);
		FScopedTransaction _ { LOCTEXT("SetLocalImplementation", "Set Implementation Graph") };
		stateNode->Modify();

		const FName itemName = *item;
		auto selectedGraph = stateNode->GetBlueprint()->UbergraphPages.FindByPredicate(
			[&itemName](UEdGraph* graph) { return graph->GetFName() == itemName; });
		stateNode->localImplementationGraph.SetGraph(selectedGraph ? *selectedGraph : nullptr);

		if (auto schema = stateNode->GetSchema())
		{
			schema->ForceVisualizationCacheClear();
		}
	}

	void SubmachineNameChanged(TSharedPtr<FName> item, ESelectInfo::Type)
	{
		UGCFSMStateNode* stateNode = CastChecked<UGCFSMStateNode>(GraphNode);
		FScopedTransaction _ { LOCTEXT("SetSubmachine", "Set Submachine FSM") };
		stateNode->Modify();

		TArray<UGCFSMStartNode*> nodes;
		FBlueprintEditorUtils::GetAllNodesOfClass(stateNode->GetBlueprint(), nodes);
		const FString itemName = item->ToString();
		auto selectedNode = nodes.FindByPredicate(
			[&itemName](UGCFSMStartNode* node) { return node->GetFriendlyName() == itemName; });
		stateNode->submachineNodeGuid = selectedNode ? (*selectedNode)->NodeGuid : FGuid {};

		if (auto schema = stateNode->GetSchema())
		{
			schema->ForceVisualizationCacheClear();
		}
	}
