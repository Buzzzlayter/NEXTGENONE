UGCFSMStateNode::UGCFSMStateNode(const FObjectInitializer& objectInitializer)
	: Super(objectInitializer)
{
	transitionEvents.Add("Event");

#if WITH_EDITORONLY_DATA
	bCanRenameNode = true;
#endif
}

void UGCFSMStateNode::Serialize(FArchive& ar)
{
	Super::Serialize(ar);

	// backward compatibility code
	if (ar.IsLoading() && localState_DEPRECATED)
	{
		localState_DEPRECATED = false;
		nodeKind = EGCFSMStateNodeKind::Local;
	}
}

void UGCFSMStateNode::AllocateDefaultPins()
{
	UGCFSMNodeBase::AllocateDefaultPins();

	auto schema = GetDefault<UEdGraphSchema_K2>();

	// Add the implementation pin (regular states only)
	check(Pins.Num() == ImplementationPinIndex);
	auto pin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Class, {}, UGCFSMState::StaticClass(), "ImplementationClass");
	pin->PinFriendlyName = LOCTEXT("ImplementationPin", "Implementation Class");
	pin->PinToolTip = LOCTEXT("ImplementationPin.Tooltip", "A class that will be instantantied to handle this state logic").ToString();
	pin->bHidden = !IsRegularState();

	// Add the pin for replication option (submachine states only)
	check(Pins.Num() == ReplicationOptionPinIndex);
	pin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Byte, {}, EnumHelpers::GetUEnum<EGCFSMSubmachineReplicationOptions>(), "replication");
	pin->bHidden = !IsSubmachineState();
	schema->SetPinAutogeneratedDefaultValueBasedOnType(pin);

	// Add the hidden replicated exit pin
	check(Pins.Num() == ReplicatedExitPinIndex);
	pin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, {}, nullptr);
	pin->bHidden = true;

	// Add transition pins for each name
	check(Pins.Num() == TransitionsPinStartIndex);
	for (auto name : transitionEvents)
	{
		pin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, {}, nullptr, name);
		pin->PinToolTip = LOCTEXT("TransitionPin.Tooltip", "Transition pin\n").ToString() + name.ToString();
	}

	// Add the Timed Event pins
	pin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, {}, nullptr, GetTimeOutEventPinName());
	pin->PinToolTip = LOCTEXT("TimeOutEvent.Tooltip", "This transition is triggered after the timeout has expired").ToString();
	pin->bHidden = true;
	pin = CreatePin(EGPD_Input, UEdGraphSchema_K2::PC_Real, UEdGraphSchema_K2::PC_Float, nullptr, "TimeOut");
	pin->PinToolTip = LOCTEXT("TimeOut.Tooltip", "If greater than 0, the state is automatically exited after the specified time in seconds").ToString();
	pin->bHidden = true;
	schema->SetPinAutogeneratedDefaultValueBasedOnType(pin);
	SetTimedEvenPinFriendlyName();

	// Add the Tick pins
	pin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Exec, {}, nullptr, GetTickPinName());
	pin->PinFriendlyName = FText::FromString(TEXT("Tick"));
	pin->PinToolTip = LOCTEXT("TickPin.Tooltip", "This pin is executed every frame while this state is active").ToString();
	pin->bHidden = true;
	pin = CreatePin(EGPD_Output, UEdGraphSchema_K2::PC_Real, UEdGraphSchema_K2::PC_Float, nullptr, "DeltaTime");
	pin->PinToolTip = LOCTEXT("DeltaTimePin.Tooltip", "Delta time in seconds since the last Tick event. The value is valid only when the Tick pin is executed").ToString();
	pin->bHidden = true;
}

UEdGraphPin* UGCFSMStateNode::GetTimeOutEventPin() const
{
	return Pins[TransitionsPinStartIndex + transitionEvents.Num()];
}

UEdGraphPin* UGCFSMStateNode::GetTimeOutPin() const
{
	return Pins[TransitionsPinStartIndex + transitionEvents.Num() + 1];
}

UEdGraphPin* UGCFSMStateNode::GetTickPin() const
{
	return Pins[TransitionsPinStartIndex + transitionEvents.Num() + 2];
}

UEdGraphPin* UGCFSMStateNode::GetDeltaTimePin() const
{
	return Pins[TransitionsPinStartIndex + transitionEvents.Num() + 3];
}

UGCFSMStateNode::ERedirectType UGCFSMStateNode::DoPinsMatchForReconstruction(const UEdGraphPin* newPin, int32 newPinIndex, const UEdGraphPin* oldPin, int32 oldPinIndex) const
{
	// This check is to match pins whose name has been edited by the user
	if (newPinIndex == indexOfEventPinBeingRenamed && newPinIndex == oldPinIndex)
	{
		return ERedirectType::ERedirectType_Value;
	}
	else
	{
		return Super::DoPinsMatchForReconstruction(newPin, newPinIndex, oldPin, oldPinIndex);
	}
}

static void ShowPin(UEdGraphPin* pin, bool visible)
{
	if (pin->bHidden == visible)
	{
		pin->Modify();
		pin->bHidden = !visible;
	}

	if (!visible)
	{
		pin->BreakAllPinLinks();
	}
}

void UGCFSMStateNode::SetTimedEvenPinFriendlyName()
{
	auto eventPin = GetTimeOutEventPin();
	if (GetTimeOutPin()->LinkedTo.Num() > 0 || timeOut <= 0)
	{
		// timeout is unknown or invalid
		eventPin->PinFriendlyName = LOCTEXT("TimePinLabel.Dynamic", "After ?s");
	}
	else
	{
		// timeout is known
		auto str = FString::Printf(TEXT("%.2f"), timeOut);
		if (!str.RemoveFromEnd(".00"))
		{
			str.RemoveFromEnd("0");
		}
		FFormatNamedArguments args;
		args.Add(TEXT("Time"), FText::FromString(str));
		eventPin->PinFriendlyName = FText::Format(LOCTEXT("TimePinLabel.Static", "After {Time}s"), args);
	}
}

void UGCFSMStateNode::PostPlacedNewNode()
{
	Super::PostPlacedNewNode();
	SyncAllPins();
}

void UGCFSMStateNode::PostReconstructNode()
{
	// Show/hide optional pins
	SyncAllPins();
	ShowPin(GetTimeOutEventPin(), timeOut > 0 || GetTimeOutPin()->LinkedTo.Num());
	ShowPin(GetTickPin(), showTickFunction);
	ShowPin(GetDeltaTimePin(), showTickFunction);
	SetTimedEvenPinFriendlyName();

	Super::PostReconstructNode();

	// Reset these, reconstruction is finished
	indexOfEventPinBeingRenamed = -1;
	bReconstructNode = false;
}

void UGCFSMStateNode::PostEditChangeProperty(FPropertyChangedEvent& propertyChangedEvent)
{
	if (propertyChangedEvent.Property)
	{
		SyncPinWithProperty(propertyChangedEvent.Property);

		FName propertyName = propertyChangedEvent.Property->GetFName();
		if (propertyName == "transitionEvents")
		{
			if (propertyChangedEvent.ChangeType == EPropertyChangeType::ValueSet)
			{
				// An event has been renamed, save the index so that we can properly match pins during reconstruction
				indexOfEventPinBeingRenamed = TransitionsPinStartIndex + propertyChangedEvent.GetArrayIndex("transitionEvents");
			}
		}

		if ((propertyChangedEvent.ChangeType & EPropertyChangeType::Interactive) == 0)
		{
			ReconstructNode();
		}
	}

	Super::PostEditChangeProperty(propertyChangedEvent);
}

