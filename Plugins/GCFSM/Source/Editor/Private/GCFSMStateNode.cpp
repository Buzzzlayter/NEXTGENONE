FText UGCFSMStateNode::GetDefaultTitle() const
{
	return IsLocalState() ? LOCTEXT("DefaultTitle.Local", "FSM Local State") :
		IsSubmachineState() ? LOCTEXT("DefaultTitle.Submachine", "FSM Submachine State") :
		LOCTEXT("DefaultTitle", "FSM State");
}

FString UGCFSMStateNode::SuggestedNodeName() const
{
	if (IsLocalState())
	{
		auto graph = localImplementationGraph.GetGraph();
		if (IsValid(graph))
		{
			return graph->GetName();
		}
	}
	else if (IsSubmachineState())
	{
		auto node = GetSubmachineNode();
		return node ? node->GetFriendlyName() : FString {};
	}
	else if (auto implementationClass = GetImplementationClass())
	{
		return implementationClass->GetDisplayNameText().ToString();
	}
	return {};
}

FText UGCFSMStateNode::GetNodeTitle(ENodeTitleType::Type titleType) const
{
	FText defaultTitle = GetDefaultTitle();
	FString name = NodeName.Len() > 0 ? NodeName : SuggestedNodeName();
	if (name.Len() > 0)
	{
		if (titleType == ENodeTitleType::EditableTitle)
		{
			return FText::FromString(name);
		}
		else if (titleType == ENodeTitleType::FullTitle)
		{
			return FText::FromString(name + "\n" + defaultTitle.ToString());
		}
	}
	return defaultTitle;
}

